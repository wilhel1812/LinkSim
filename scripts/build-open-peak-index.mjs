#!/usr/bin/env node

import { createHash } from "node:crypto";
import { writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const SOURCE_URL = "https://raw.githubusercontent.com/open-peaks/data/master/_index.geojson";
const OUT_FILE = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../src/data/openPeakMapIndex.ts");

const normalizeName = (value) => String(value ?? "").trim();
const toFiniteNumber = (value) => {
  const parsed = Number(value);
  return Number.isFinite(parsed) ? parsed : null;
};

const keyFor = (lat, lon) => `${Math.floor(lat)}:${Math.floor(lon)}`;

const asCodeIdentifier = (name) =>
  String(name)
    .toUpperCase()
    .replace(/[^A-Z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "");

const buildId = ({ name, lat, lon, region }) => {
  const latToken = Math.round(lat * 1e4);
  const lonToken = Math.round(lon * 1e4);
  const regionToken = asCodeIdentifier(region || "WORLD").slice(0, 16);
  const nameToken = asCodeIdentifier(name).slice(0, 36);
  return `${regionToken}_${nameToken}_${latToken}_${lonToken}`;
};

const run = async () => {
  const response = await fetch(SOURCE_URL);
  if (!response.ok) {
    throw new Error(`Failed to download OpenPeaks index: ${response.status} ${response.statusText}`);
  }
  const raw = await response.text();
  const hash = createHash("sha256").update(raw).digest("hex");
  const payload = JSON.parse(raw);
  const features = Array.isArray(payload?.features) ? payload.features : [];

  const buckets = new Map();
  const dedupe = new Set();
  let skipped = 0;

  for (const feature of features) {
    const coords = feature?.geometry?.coordinates;
    if (!Array.isArray(coords) || coords.length < 2) {
      skipped += 1;
      continue;
    }
    const lon = toFiniteNumber(coords[0]);
    const lat = toFiniteNumber(coords[1]);
    const props = feature?.properties ?? {};
    const name = normalizeName(props.name);
    if (lat === null || lon === null || !name) {
      skipped += 1;
      continue;
    }
    const region = normalizeName(Array.isArray(props.regions) ? props.regions[0] : "");
    const countries = Array.isArray(props.countries) ? props.countries.map((value) => normalizeName(value)).filter(Boolean) : [];
    const meters = toFiniteNumber(props.meters);
    const id = buildId({ name, lat, lon, region: countries[0] ?? props.continent ?? "world" });
    const dedupeKey = `${name.toLowerCase()}|${lat.toFixed(5)}|${lon.toFixed(5)}`;
    if (dedupe.has(dedupeKey)) continue;
    dedupe.add(dedupeKey);
    const key = keyFor(lat, lon);
    const list = buckets.get(key) ?? [];
    list.push({
      id,
      name,
      lat: Number(lat.toFixed(6)),
      lon: Number(lon.toFixed(6)),
      elevationM: meters !== null ? Math.round(meters) : null,
      country: countries[0] || null,
    });
    buckets.set(key, list);
  }

  const bucketEntries = [...buckets.entries()]
    .sort((a, b) => a[0].localeCompare(b[0]))
    .map(([bucketKey, entries]) => [
      bucketKey,
      entries.sort((a, b) => a.name.localeCompare(b.name) || a.lat - b.lat || a.lon - b.lon),
    ]);

  const lines = [];
  lines.push("/* eslint-disable */");
  lines.push("// Auto-generated by scripts/build-open-peak-index.mjs");
  lines.push(`// Source: ${SOURCE_URL}`);
  lines.push("");
  lines.push("export type OpenPeakMapIndexEntry = {");
  lines.push("  id: string;");
  lines.push("  name: string;");
  lines.push("  lat: number;");
  lines.push("  lon: number;");
  lines.push("  elevationM: number | null;");
  lines.push("  country: string | null;");
  lines.push("};");
  lines.push("");
  lines.push("export const OPEN_PEAK_MAP_INDEX_META = {");
  lines.push(`  sourceUrl: ${JSON.stringify(SOURCE_URL)},`);
  lines.push(`  sourceSha256: ${JSON.stringify(hash)},`);
  lines.push(`  generatedAt: ${JSON.stringify(new Date().toISOString())},`);
  lines.push(`  featureCount: ${dedupe.size},`);
  lines.push(`  bucketCount: ${bucketEntries.length},`);
  lines.push(`  skippedCount: ${skipped},`);
  lines.push("} as const;");
  lines.push("");
  lines.push("export const OPEN_PEAK_MAP_INDEX_BUCKETS: Record<string, OpenPeakMapIndexEntry[]> = {");
  for (const [bucketKey, entries] of bucketEntries) {
    lines.push(`  ${JSON.stringify(bucketKey)}: ${JSON.stringify(entries)},`);
  }
  lines.push("};");
  lines.push("");

  await writeFile(OUT_FILE, `${lines.join("\n")}\n`, "utf8");
  process.stdout.write(
    `Generated ${path.relative(process.cwd(), OUT_FILE)} with ${dedupe.size} peaks in ${bucketEntries.length} buckets.\n`,
  );
};

run().catch((error) => {
  process.stderr.write(`${error instanceof Error ? error.message : String(error)}\n`);
  process.exitCode = 1;
});

